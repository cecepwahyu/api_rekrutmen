package com.rekrutmen.rest_api.dto;

public class ResetPasswordRequest {

    private String email;
    private String nik;

    // Getters and setters
    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }
}
